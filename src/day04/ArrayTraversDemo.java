package day04;

public class ArrayTraversDemo {
    public static void main(String[] args) {
        int [] arr1 = {10, 20, 30, 40, 50};

        int[] arr2 = new int[5];

        arr2[0] = 100;
        arr2[1] = 200;
        arr2[2] = 300;
        arr2[3] = 400;
        arr2[4] = 500;

        System.out.println(arr1[0]);
        System.out.println(arr2[2]);
    }
}
